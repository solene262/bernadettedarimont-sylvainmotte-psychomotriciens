            <p class="text-justify">
            La psychomotricité est une approche développementale de l'enfant, dans sa globalité.
            Elle utilise le langage du corps et agit sur celui-ci pour aider l'enfant à harmoniser ses fonctions motrices, instrumentales, cognitives et affectives.
            Elle passe par la médiation telle que l'activité motrice spontanée, le jeu sensori-moteur, les techniques corporelles de stimulation, d'expression ou de relaxation...
            <br>         
            Un suivi en psychomotricité est souvent complémentaire à l'intervention d'autres professionnels de l'enfance.
            </p>
            <p class="text-center2">
            Accueillir l'enfant dans sa globalité, le rejoindre dans ses difficultés et 
            ses aptitudes à travers une écoute sensible du corps et peu à peu l'aider à 
            se dégager de son blocage lié 
            à son traumatisme ou son handicap pour l'amener à devenir acteur de sa vie.
            </p>
