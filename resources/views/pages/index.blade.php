<section class="about" id="conditions">
    <h3 class="text-center">Conditions et modalités</h3>
    <div class="d-flex">
        <div class="p-2 flex-grow-1">
            <p class="text-justify">
            Une prescription médicale de kinésithérapie en rééducation psychomotrice du médecin traitant ou d'un médecin 
            spécialisé (pédiatre, pédopsychiatre, neuropédiatre...) sera nécessaire en vue d'une collaboration médicale et d'un remboursement par
            la mutuelle.  
            </p>
            <p class="text-justify">
            Une rencontre initiale avec les parents va leur permettre de décrire leur enfant au niveau 
            de son histoire, de ses difficultés et de ses compétences, de ses centres d'intérêts...
            L'entretien sera aussi l'occasion d'accueillir la demande et d'expliquer notre travail.
            </p>
            <p class="text-justify">
            Un bilan est ensuite réalisé en 3-4 séances. Ce bilan utilisant l'observation interactive et les tests standardisés va permettre,
            à travers l'évaluation de l'enfant, de donner des repères pour le travail ultérieur.
            Les conclusions tirées à partir d'une synthèse des résultats obtenus et croisés avec les comportements observés 
            seront remises aux parents.
            </p>
            <p class="text-justify">
            Tous ces éléments présentés vont donner des pistes d'intervention et permettre d'envisager un suivi, en collaboration
            avec les parents.
            <br>
            Les séances se déroulent sur une durée de 45min, à raison d'1 à 2 fois par semaine.
            </p>
            <p class="text-justify">
            Des rencontres régulières ou occasionnelles seront fixées dès le début du suivi pour évoquer l'évolution de l'enfant 
            dans les séances, ainsi que dans la famille, voire dans ses différents milieux de vie.
            </p>
        </div>
    </div>
</section>

