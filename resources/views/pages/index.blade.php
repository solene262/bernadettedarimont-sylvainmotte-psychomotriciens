<section class="text-and-image image-left" id="objectifs">
  <div class="text">
  <h4>Objectifs</h4>
        <p class="text-justify">
        Le suivi en psychomotricité va permettre à l'enfant de développer son outillage corporel, d'affiner
        ses sensations aussi bien que ses perceptions, de renforcer une image plus valorisante de lui-même,
        de prendre conscience de ses forces et de son potentiel, de s'exprimer à travers et par son corps,
        de vivre des relations positives et significatives avec le milieu tout un sentant et en maîtrisant aussi
        bien l'espace que le temps.
        </p>
        <p class="text-justify">
        A travers les activités psychomotrices, l'enfant va pouvoir expérimenter une gamme de possibilités sensori-motrices,
        interactives et ceci de façon progressive et en rapport avec son niveau de maturité.
        <br>
        En renforçant ses conduites motrices de base, de multiples occasions seront données à l'enfant :
        </p>
            <ul>
            <li>d'améliorer les déplacements divers et les habiletés globales</li>
            <li>de développer la sensibilité proprioceptive et de réguler le tonus postural</li>
            <li>d'affronter l'espace, de s'y localiser</li>
            <li>d'améliorer l'image qu'il a de son corps en mouvement et de prendre conscience de son moi corporel</li>
            <li>d'aider à la structuration du temps</li>
            <li>d'aider à la latéralisation (espace gauche-droite)</li>
            <li>d'améliorer la socialisation et l'autonomie</li>
            </ul>
        <p class="text-justify">
        Les séances se déroulent, dans un premier temps, en individuel puis en petit groupe (2-3 enfants), offrant
        ainsi à l'enfant l'équilibre nécessaire entre l'épanouissement individuel et la valorisation des relations
        interpersonnelles.
        </p>
  </div>
