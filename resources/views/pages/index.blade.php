    <p class="text-justify">
        Chez l'enfant, âgé de 0 à 12 ans, les indications sont nombreuses et variées.
    </p>
    <ul>
    <li>Troubles sensori-moteurs (sensoriels, proprioceptifs, posturaux...).</li>
    <li>Troubles envahissants du développement (autisme, hyperactivité...)
    <li>Troubles tonico-émotionnels (intégration des affects, gestion des émotions)</li>
    <li>Troubles neuro-moteurs (infirmité motrice cérébrale, hémiplégie, épilepsie...)</li>
    <li>Troubles du caractère et de la personnalité</li>
    <li>Troubles instrumentaux (dyspraxie, dyslexie...)</li>
    <li>Défaut d'attention et de concentration</li>
  </ul>
